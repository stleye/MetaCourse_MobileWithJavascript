for (var i = 1; i <= 5; i++) {
    console.log(i);
}
console.log('Counting completed!')

for (var i = 5; i >= 1; i--) {
    console.log(i)
}
console.log('Countdown finished!')

var counter = 1;
while (counter <= 5) {
    console.log(counter);
    counter += 1
}
console.log('Counting completed!')

var counter = 5;
while (counter >= 1) {
    console.log(counter);
    counter -= 1
}
console.log('Countdown finished!')

